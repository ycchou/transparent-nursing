export function icon(name, opts = {}) {
  const { size = 20, className = '', stroke = 'currentColor' } = opts;
  const body = ICONS[name];
  if (!body) {
    console.warn('Missing icon:', name);
    return '';
  }
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none"
            stroke="${stroke}" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"
            class="${className}">${body}</svg>`;
}

/** Replace all <i data-icon="name"></i> placeholders with inline SVG */
export function renderIcons(root = document) {
  root.querySelectorAll('[data-icon]').forEach((el) => {
    const name = el.dataset.icon;
    const size = el.dataset.size || 20;
    const cls = el.className || '';
    el.outerHTML = icon(name, { size: +size, className: cls });
  });
}
